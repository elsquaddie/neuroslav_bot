            update_data = json.loads(post_body.decode('utf-8'))
            
            # –ó–∞–ø—É—Å–∫–∞–µ–º –∞—Å–∏–Ω—Ö—Ä–æ–Ω–Ω—É—é –ª–æ–≥–∏–∫—É –±–æ—Ç–∞
